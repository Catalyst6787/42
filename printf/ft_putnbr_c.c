#include "header.h"

void	ft_putnbr_c(int n)
{
	if (n == -2147483648)
	{
		ft_putstr_c("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar_c('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_c(n / 10);
		n = n % 10;
	}
	if (n < 10)
		ft_putchar_c((n) + '0');
}
